// ── Helpers ───────────────────────────────────────────────────────────────────

function extractDateStr(s) {
  s = String(s).trim()
  const iso = s.match(/^(\d{4})-(\d{2})-(\d{2})/)
  if (iso) return `${iso[1]}-${iso[2]}-${iso[3]}`
  const us  = s.match(/^(\d{1,2})\/(\d{1,2})\/(\d{4})/)
  if (us)  return `${us[3]}-${us[1].padStart(2,'0')}-${us[2].padStart(2,'0')}`
  return null
}

async function supabase(method, path, body) {
  const res = await fetch(`${SUPABASE_URL}${path}`, {
    method,
    headers: {
      apikey: SERVICE_KEY,
      Authorization: `Bearer ${SERVICE_KEY}`,
      'Content-Type': 'application/json',
      Prefer: method === 'POST' ? 'resolution=merge-duplicates,return=representation' : '',
    },
    body: body ? JSON.stringify(body) : undefined,
  })
  const text = await res.text()
  if (!res.ok) throw new Error(`${method} ${path} → ${res.status}: ${text}`)
  return text ? JSON.parse(text) : null
}

// ── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  // 1. Fetch all profiles to build name → {user_id, full_name} map
  console.log('Fetching profiles...')
  const profiles = await supabase('GET', `/rest/v1/profiles?company_id=eq.${COMPANY_ID}&select=id,full_name`)
  if (!profiles || !profiles.length) throw new Error('No profiles found — check COMPANY_ID and SERVICE_KEY')

  // Build case-insensitive partial match map
  const profileMap = new Map()
  for (const p of profiles) {
    profileMap.set(p.full_name.toLowerCase(), { user_id: p.id, full_name: p.full_name })
  }

  console.log(`Found ${profiles.length} profiles:`, profiles.map(p => p.full_name).join(', '))

  // Maps RAW submission names → exact profile full_name (for spelling mismatches)
  const ALIASES = {
    'mohamed shabaan': 'Mohamed sha3ban',
    'mohamed sayed':   'Muhamed Sayed',
    'yasmine':         'Yasmina Ali',
  }

  function findProfile(name) {
    const lower = name.toLowerCase().trim()
    // Alias map first
    const aliased = ALIASES[lower]
    if (aliased && profileMap.has(aliased.toLowerCase())) return profileMap.get(aliased.toLowerCase())
    // Exact match
    if (profileMap.has(lower)) return profileMap.get(lower)
    // Partial match: profile name starts with the raw name or vice versa
    for (const [key, val] of profileMap) {
      if (key.startsWith(lower) || lower.startsWith(key)) return val
    }
    return null
  }

  // 2. Deduplicate: keep latest submission per (agentName, reportDate)
  const latest = {}   // key = "name|date"
  for (const r of RAW) {
    const dateStr = extractDateStr(r[2])
    if (!dateStr) continue
    const key = `${r[1]}|${dateStr}`
    const ts  = new Date(r[0]).getTime()
    if (!latest[key] || ts > latest[key].ts) {
      latest[key] = { ts, r, dateStr }
    }
  }

  const rows = Object.values(latest)
  console.log(`\nProcessing ${rows.length} unique agent-day submissions...`)

  let inserted = 0, skipped = 0
  const skippedNames = new Set()

  for (const { r, dateStr } of rows) {
    const [_ts, rawName, _dateField,
      sheets, posts, requests, followups, totalLeads, reached, notReached,
      crmAct, uploaded, notUploaded, summary,
      m1n, m1p, m1r, m2n, m2p, m2r, m3n, m3p, m3r
    ] = r

    const profile = findProfile(rawName)
    if (!profile) {
      skippedNames.add(rawName)
      skipped++
      continue
    }

    // Build missed_calls array (only non-empty entries)
    const missed_calls = []
    const missedGroups = [[m1n,m1p,m1r],[m2n,m2p,m2r],[m3n,m3p,m3r]]
    for (const [n, p, reason] of missedGroups) {
      if (n && String(n).trim()) {
        missed_calls.push({ name: String(n).trim(), phone: String(p||'').trim(), reason: String(reason||'').trim() })
      }
    }

    const has_missed_uploads = notUploaded > 0

    await supabase('POST', '/rest/v1/bay_reports?on_conflict=user_id,report_date', {
      user_id:            profile.user_id,
      company_id:         COMPANY_ID,
      full_name:          profile.full_name,
      report_date:        dateStr,
      sheets:             Number(sheets)      || 0,
      posts:              Number(posts)       || 0,
      requests:           Number(requests)    || 0,
      followups:          Number(followups)   || 0,
      total_leads:        Number(totalLeads)  || 0,
      reached:            Number(reached)     || 0,
      not_reached:        Number(notReached)  || 0,
      crm_actions:        Number(crmAct)      || 0,
      uploaded:           Number(uploaded)    || 0,
      not_uploaded:       Number(notUploaded) || 0,
      crm_confirm:        false,
      has_missed_uploads,
      missed_calls,
      summary:            String(summary || ''),
    })

    console.log(`  ✅ ${dateStr}  ${profile.full_name}`)
    inserted++
  }

  console.log(`\nDone! ${inserted} rows inserted/updated.`)
  if (skipped > 0) {
    console.warn(`⚠️  ${skipped} rows skipped — no profile match for: ${[...skippedNames].join(', ')}`)
    console.warn('   → Add those agents to Supabase and re-run to backfill their data.')
  }
}

main().catch(e => { console.error('\n❌', e.message); process.exit(1) })
